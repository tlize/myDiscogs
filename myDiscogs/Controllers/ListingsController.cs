using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using System.Web.UI;
using myDiscogs.Data;
using myDiscogs.DiscogsApi;
using myDiscogs.Models.Collection;
using myDiscogs.Models.Inventory;
using Newtonsoft.Json;

namespace myDiscogs.Controllers
{
    public class ListingsController : Controller
    {
        private myDiscogsContext db = new myDiscogsContext();
        private DiscogsClient client = new DiscogsClient();

        // GET: Listings
        public ActionResult Index()
        {
            var soldItemsData = client.GetSoldItems();
            var soldItems = JsonConvert.DeserializeObject<PaginatedItems>(soldItemsData);

            return View(soldItems.Listings);
        }

        // GET: Listings/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Listing listing = db.Listings.Find(id);
            if (listing == null)
            {
                return HttpNotFound();
            }
            return View(listing);
        }

        // GET: Listings/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Listings/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id,ResourceUrl,Uri,Status,Condition,SleeveCondition,Comments,ShipsFrom,Posted,AllowOffers,OfferSubmitted,Audio,Price,OriginalPrice,ShippingPrice,OriginalShippingPrice,InCart,Weight,EstimatedWeight,FormatQuantity,ExternalId,Location,Quantity")] Listing listing)
        {
            if (ModelState.IsValid)
            {
                db.Listings.Add(listing);
                db.SaveChanges();
                return RedirectToAction("Index");
            }

            return View(listing);
        }

        // GET: Listings/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Listing listing = db.Listings.Find(id);
            if (listing == null)
            {
                return HttpNotFound();
            }
            return View(listing);
        }

        // POST: Listings/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,ResourceUrl,Uri,Status,Condition,SleeveCondition,Comments,ShipsFrom,Posted,AllowOffers,OfferSubmitted,Audio,Price,OriginalPrice,ShippingPrice,OriginalShippingPrice,InCart,Weight,EstimatedWeight,FormatQuantity,ExternalId,Location,Quantity")] Listing listing)
        {
            if (ModelState.IsValid)
            {
                db.Entry(listing).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(listing);
        }

        // GET: Listings/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Listing listing = db.Listings.Find(id);
            if (listing == null)
            {
                return HttpNotFound();
            }
            return View(listing);
        }

        // POST: Listings/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Listing listing = db.Listings.Find(id);
            db.Listings.Remove(listing);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
